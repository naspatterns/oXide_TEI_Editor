import { defineConfig } from 'vite';
import react from '@vitejs/plugin-react';

export default defineConfig({
  plugins: [react()],
  base: './',
  esbuild: {
    drop: ['console', 'debugger'],  // Remove console.* and debugger in production
  },
  build: {
    rollupOptions: {
      output: {
        manualChunks: (id) => {
          if (id.includes('node_modules')) {
            if (
              id.includes('@codemirror') ||
              id.includes('@uiw/react-codemirror') ||
              id.includes('codemirror')
            ) {
              return 'codemirror';
            }
            if (id.includes('react-dom') || id.includes('/react/')) {
              return 'react';
            }
          }
          // Split heavy schema data into its own chunk so the main bundle
          // is smaller, parses faster, and the schema can be cached
          // independently across releases.
          if (id.includes('src/schema/teiP5Generated')) {
            return 'tei-p5';
          }
          if (id.includes('src/schema/teiAutoGenerated')) {
            return 'tei-auto';
          }
          return undefined;
        },
      },
    },
  },
});
